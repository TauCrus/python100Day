'''
寻找“水仙花数”
水仙花数是指一个 3 位数，
它的每个位上的数字的 3次幂之和等于它本身
（例如：1^3 + 5^3+ 3^3 = 153）
'''

for i in range(100, 1000):
    a = i // 100              # 取百位
    b = (i - a * 100) // 10   # 取十位
    c = i % 10                # 取个位
    d = pow(a, 3) + pow(b, 3) + pow(c, 3)

    if i == d:
        print('%d是水仙花数' % i)